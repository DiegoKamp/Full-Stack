/*Expressões e Operadores*/


// Operadores aritméticos
console.log("Soma:", 12 + 9);
console.log("Concatenação:", "12" + 9);

console.log("Subtração:", 12 - 9);

console.log("Multiplicação:", 12*9);

console.log("Divisão:", 12/9);

console.log("Resto da divisão:", 12%9);

console.log("Potenciação:", 12**9);


// Incremento e Decremento

let number = 5;

console.log(number);

// Incremento
number = number + 1;

console.log(number);

number++;

console.log(number);

console.log(number++); // O incremento é feito após aparecer no console.

console.log(number);

console.log(++number); // O incremento é feito antes do console, por isso aparecerá


// Decremento
number = number -1;
console.log(number);

number--;

console.log(number);

console.log(number--); // O decremento é feito após aparecer no console.

console.log(number);

console.log(--number); // O incremento é feito antes do console, por isso aparecerá

number += 30;

console.log(number);

number -= 29;

console.log(number);

number *=2;

console.log(number);

number **=2;

console.log(number);

number /=3;

console.log(number);

